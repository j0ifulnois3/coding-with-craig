package com.pluralsight;

import org.w3c.dom.ls.LSOutput;

public class Arrays {
    public static void main(String[] args) {

//        Create Index/Order is 0-5

        String[] Feelings = {"Mad", "Sad", "Happy", "Elated", "Overjoyed", "Disappointed"};

//        To print one thing at a time from the index
        System.out.println(Feelings[5]);

//    To identify length of list
        System.out.println(Feelings.length);

//        To print all the names for an array/will continue to reflect array when updated
        for (int index = 0; index < Feelings.length;index++){
            System.out.println(Feelings[index]);
        }






    }





































}
